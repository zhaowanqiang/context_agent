"""
content_agent_v1.py
@zynqorw 内容生产流水线 —— 第一版（先跑通「生成」这一步）

设计：两跳生成 + 一道事实闸门。
  Hop 1  出结构（大纲）  ←── 人工在这里改一次，改完再往下
  Hop 2  填成稿
  Gate   把硬信息拎出来待人工确认（金融内容不能省）

先在这里手动跑，稳定后再包成 Python 服务 + Next.js 后台。
运行前：pip install anthropic；export ANTHROPIC_API_KEY=...
"""

import os
import anthropic

client = anthropic.Anthropic(api_key=os.environ["ANTHROPIC_API_KEY"])
MODEL = "claude-opus-4-8"          # 结构+成稿用强模型
GATE_MODEL = "claude-haiku-4-5-20251001"  # 事实闸门用小模型省钱


# ── 风格指纹（从 style.md 精简嵌入。改 style.md 后同步这段）──────────────
STYLE_GUIDE = """
结构骨架（严格按顺序）：
1. 标题：[产品名] + 核心动作 + 简单/教程
2. 一句话产品定义：是什么 + 谁背书 + 绑定什么账户 + 形态 + 核心用途
3. 过渡句：进入正文
4. 分步正文：每步 1 段，每步至少埋一个「实测才知道」的点（坑/技巧/工具）
5. 实测收尾：第一人称亲测体验，打消疑虑
6. CTA：稀缺资源 + 关注留人，与开头呼应

语言指纹：
- 短句为主，口语化，像说给朋友听
- 第一人称实测口吻：我使用 / 我这边 / 我测试一下
- 降门槛口头禅：一步一步来、就 OK 了、很方便、很丝滑、不用多说
- 不用 emoji；术语（IBAN/USDT/KYC）自然带入不过度解释
- 重点用「最重要的是 / 最难的一步 / 关键」标记

红线：不写伪造/购买证件、造假绕过 KYC 的内容。合规的坑+技巧照写。
"""

# ── few-shot：你自己表现最好的成稿（先放 1 条种子，逐步补到 5-8 条）────────
# 注：种子已按红线清洗，证件相关只保留合规表述。
FEWSHOT = """
【范例 1 · 浏览量最高】
Plasma One Card 开卡简单教程

Plasma One Card 是一张 Visa 借记卡，背后由 Fiat24 提供支持，绑定的是一个欧洲 IBAN 账户，
提供实体卡 + 虚拟卡，支持 USDT 等加密货币充值转换为法币使用。

开卡的具体流程：

下载 Plasma One 的 App，首先最重要的是获取一个创建账户的邀请码，数量有限，获取有点难度，可以这样——
直接向 Grok 要邀请码，AI 检索速度比你快得多，每次给的几个可能是无效的，多试几次就 OK 了。
后续我这边有邀请码会发出来。

注册地区选香港，后续要提供地址和证明，一步一步来。

按要求填个人信息，姓名一定要和护照上的一致，用英文填写，地址填香港地址。

上传护照这一步没有中国选项，默认地区香港，直接上传中国护照就能通过。
地址证明文件按官方要求准备（银行流水/账单等），根据自己实际情况来。

最后一步提交失败可以多次提交，证明文件如果是图片，最好打印出来拍照再提交，基本秒过。

开卡成功后就能快速入金，我用 USDT 测了一下，入金快且很丝滑，日常使用完全不用多说。

想开卡没邀请码的留个关注，蹲我后续发邀请码！
"""


# ── Hop 1：出结构 ────────────────────────────────────────────────────
STRUCTURE_PROMPT = """你是 @zynqorw 的内容助手。严格按下面的风格指纹，为给定素材产出一份帖子【大纲】。
只输出大纲骨架（标题 + 每步一句话要点），不写成稿。

【风格指纹】
{style}

【铁律】
- 结构必须是：标题 → 一句话产品定义 → 过渡句 → 分步正文 → 实测收尾 → CTA
- 分步正文每一步都要标出「这一步埋的坑/技巧/工具是什么」
- 涉及费率/额度/门槛/地区限制/KYC 要求等硬信息，在该步末尾加 [待核实] 标记
- 不要输出任何造假绕过实名/KYC 的内容

【原始素材】
{material}
"""

# ── Hop 2：填成稿 ────────────────────────────────────────────────────
DRAFT_PROMPT = """你是 @zynqorw 本人的写作分身。参考下面的成稿范例，模仿其语气、句长、口头禅，
把给定大纲扩写成一条完整帖子。

【风格指纹】
{style}

【成稿范例】
{fewshot}

【要写的大纲】
{outline}

【要求】
- 全程第一人称实测口吻，短句为主，不用 emoji
- 保留大纲里每一步的坑/技巧/工具
- 结尾用稀缺性钩子做 CTA
- 保留大纲里的 [待核实] 标记，绝不自己编造具体数字
"""

# ── 事实闸门：把硬信息拎出来待人工确认 ──────────────────────────────────
FACTCHECK_PROMPT = """通读下面这条成稿，把所有「硬信息」逐条列成清单，供人工发布前确认。
硬信息包括：费率/额度/门槛数字、支持地区、KYC 要求、开卡条件，
以及任何读者会当依据的事实。只列清单，每条标「需确认」，不要改写正文。

【成稿】
{draft}
"""


def call(prompt, model=MODEL):
    msg = client.messages.create(
        model=model,
        max_tokens=2000,
        messages=[{"role": "user", "content": prompt}],
    )
    return msg.content[0].text


if __name__ == "__main__":
    # 把你测某张卡 / 某个工具的原始笔记贴这里
    material = """
（原始素材：你实测某张卡的零散笔记，越具体越好——
卡种、背书方、绑定账户、开卡踩的坑、你用的技巧、入金体验……）
"""

    print("\n========== Hop 1：大纲 ==========\n")
    outline = call(STRUCTURE_PROMPT.format(style=STYLE_GUIDE, material=material))
    print(outline)

    # ★ 停在这里：人工把大纲改顺（选题角度、步骤顺序、坑够不够），改完再跑下面
    input("\n[改完大纲后按回车继续生成成稿]")

    print("\n========== Hop 2：成稿 ==========\n")
    draft = call(DRAFT_PROMPT.format(style=STYLE_GUIDE, fewshot=FEWSHOT, outline=outline))
    print(draft)

    print("\n========== Gate：待核实清单（发布前逐条确认）==========\n")
    checklist = call(FACTCHECK_PROMPT.format(draft=draft), model=GATE_MODEL)
    print(checklist)
